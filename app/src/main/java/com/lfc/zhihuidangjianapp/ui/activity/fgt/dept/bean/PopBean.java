package com.lfc.zhihuidangjianapp.ui.activity.fgt.dept.bean;

public class PopBean {
    private String id;
    private String name;

    public PopBean(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
